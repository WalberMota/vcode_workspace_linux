#include "LibMath.h"
#include <cmath>



int Somar(int num1,int num2)
{
    return num1+num2;
}

int Subtrair(int num1,int num2)
{
    return num1-num2;
}

int Multiplicar(int num1,int num2)
{
    return num1*num2;
}

float Dividir(int num1,int num2)
{
    return num1/num2;
}

float Potencia(int num1,int num2)
{
    return pow(num1,num2);
}
float RaizQuad(int num)
{
    return sqrt(num);
}
